    void findPass(String uname) {
        try {
            con = DBConnection.getConnection();
            ps = con.prepareStatement("select * from login where username=?");
            uname = txtUsername.getText();
            ps.setString(1, uname);
            rs = ps.executeQuery();
            if (rs.next()) {
                if (txtUsername.getText().equals(rs.getString(1))) {
                    rs.getString("password");
                    JOptionPane.showMessageDialog(null, "Mật khẩu của tài khoản: " + uname + " là: " + rs.getString("password"));
                    rs.getString("username");
                } else {
                    JOptionPane.showMessageDialog(this, "Tài khoản không tồn tại!");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Tài khoản không tồn tại!");
            }

        } catch (Exception e) {
        }

    }

